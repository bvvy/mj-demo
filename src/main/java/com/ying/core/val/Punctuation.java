package com.ying.core.val;

/**
 * @author bvvy
 * 标点符号
 */
public interface Punctuation {

    /**
     * 逗号
     */
    String COMMA = ",";

    /**
     * 分号
     */
    String SEMICOLON = ";";

    /**
     * 竖线
     */
    String VERTICAL = "|";

    /**
     * 斜杠
     */
    String SLASH = "/";

    /**
     * 点
     */
    String DOT = ".";

    /**
     * 减号
     */
    String MINUS = "-";

}
